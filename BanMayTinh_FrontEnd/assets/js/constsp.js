function saveProductDetails(productId) {
    // Ưu tiên tìm trong mảng products (có đầy đủ thông tin)
    let productDetails = products.find(product => product.id == productId || product.id === productId);
    
    // Nếu không tìm thấy trong products, tìm trong localStorage
    if (!productDetails) {
        const productAll = JSON.parse(localStorage.getItem('productAll')) || [];
        productDetails = productAll.find(product => product.id == productId || product.id === productId);
        
        // Nếu tìm thấy trong productAll nhưng thiếu thông tin, thử tìm lại trong products để merge
        if (productDetails) {
            const fullProductInfo = products.find(product => product.id == productId || product.id === productId);
            if (fullProductInfo) {
                // Merge thông tin: ưu tiên thông tin từ products, nhưng giữ image đã xử lý từ productAll
                productDetails = {
                    ...fullProductInfo,
                    avatar: productDetails.avatar || fullProductInfo.image || fullProductInfo.avatar,
                    image: productDetails.image || fullProductInfo.image || fullProductInfo.avatar
                };
            }
        }
    }
    
    if (productDetails) {
        // Đảm bảo có đầy đủ thông tin cần thiết
        if (!productDetails.brand) productDetails.brand = '';
        if (!productDetails.size) productDetails.size = '';
        if (!productDetails.description) productDetails.description = '';
        if (!productDetails.colors) productDetails.colors = [];
        if (!productDetails.versions) productDetails.versions = [];
        if (!productDetails.smallImages) productDetails.smallImages = [];
        
        localStorage.setItem('productDetails', JSON.stringify(productDetails));
    }
}